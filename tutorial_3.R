# Creating sequences
# Creating a sequence of numbers from 1 to 5
print(1:5)

# Using 'seq' to get a sequence where increments are more than 1
print(seq(from=1, to=5, by=2))
# In Python this would be [x for x in range(1,5,2)]

# Using 'rep' to repeat the same value a certain number of times
# Repeat 5, 5 times
print(rep(5, times=5))